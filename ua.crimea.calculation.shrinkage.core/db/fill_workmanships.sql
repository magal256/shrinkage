CREATE TABLE properties(id integer GENERATED BY DEFAULT AS IDENTITY NOT null, name varchar(250), num_value integer, date_value date);

CREATE TABLE tolerances (id integer GENERATED BY DEFAULT AS IDENTITY NOT null, 
						  name varchar(50), 
						  workmanship integer,
						  up double precision, 
						  down double precision, 
						  up_size double precision, 
						  down_size double precision, 
						  w_type integer, 
						  w_class integer,
						  primary key(id));
CREATE TABLE matter_groups (id integer GENERATED BY DEFAULT AS IDENTITY NOT null, 
					 name varchar(4000),				
					 description varchar(4000),
					 hidden BOOLEAN DEFAULT false,
					PRIMARY KEY(id));	
CREATE TABLE matters (id integer GENERATED BY DEFAULT AS IDENTITY NOT null, 
					 name varchar(4000),
					 m_type varchar(4000),
					 commercial_grade varchar(4000),
					 maker varchar(4000),
					 long_min double precision,
					 long_max double precision,
					 cros_min double precision,
					 cros_max double precision,
					 description varchar(4000),
					 group_id integer,
					PRIMARY KEY(id));					  
CREATE TABLE details(id integer GENERATED BY DEFAULT AS IDENTITY NOT null, 
					 name varchar(4000),
					 matter_id integer,
					 measuring integer,
					 description varchar(4000),
					 PRIMARY KEY(id));
CREATE TABLE calculation(id integer GENERATED BY DEFAULT AS IDENTITY NOT null, 
						 detail_id integer, 
						 c_size double precision,
						 sign_size double precision,
						 tolerance_id integer,
						 sign_tolerance_id integer,
						 calc_type integer,
						 PRIMARY KEY(id));				 